package com.github.bazukaa.nakshatra.portfolio.models;

import androidx.room.Entity;
import androidx.room.PrimaryKey;

@Entity(tableName = "user_table")
public class User {

    @PrimaryKey(autoGenerate = true)
    private int id;

    private String profileImg;
    private String name;
    private String designation;
    private String philosophy;
    private String email;
    private String github;
    private String linkedin;
    private String instagram;
    private String facebook;

    public User(String profileImg, String name, String designation, String philosophy, String email, String github, String linkedin, String instagram, String facebook) {
        this.profileImg = profileImg;
        this.name = name;
        this.designation = designation;
        this.philosophy = philosophy;
        this.email = email;
        this.github = github;
        this.linkedin = linkedin;
        this.instagram = instagram;
        this.facebook = facebook;
    }

    public String getProfileImg() {
        return profileImg;
    }

    public void setProfileImg(String profileImg) {
        this.profileImg = profileImg;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDesignation() {
        return designation;
    }

    public void setDesignation(String designation) {
        this.designation = designation;
    }

    public String getPhilosophy() {
        return philosophy;
    }

    public void setPhilosophy(String philosophy) {
        this.philosophy = philosophy;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getGithub() {
        return github;
    }

    public void setGithub(String github) {
        this.github = github;
    }

    public String getLinkedin() {
        return linkedin;
    }

    public void setLinkedin(String linkedin) {
        this.linkedin = linkedin;
    }

    public String getInstagram() {
        return instagram;
    }

    public void setInstagram(String instagram) {
        this.instagram = instagram;
    }

    public String getFacebook() {
        return facebook;
    }

    public void setFacebook(String facebook) {
        this.facebook = facebook;
    }
}
